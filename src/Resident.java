public class Resident {
    private int flat;
    private int lvl;
    private int id;
    public Resident(int flat, int lvl, int id) {
        this.flat = flat;
        this.lvl = lvl;
        this.id = id;
    }

    public int getFlat() {
        return flat;
    }

    public int getLvl() {
        return lvl;
    }

    public int getId() {
        return id;
    }
}

